char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (a <= i)
	{
		dest[a] = src[a];
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
