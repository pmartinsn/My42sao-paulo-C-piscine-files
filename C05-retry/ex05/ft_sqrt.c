int	ft_sqrt(int nb)
{
	int i;
	int j;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 1;
	while (i <= (nb / 2) && j < nb)
	{
		j = i * i;
		if (j == nb)
			return (i);
		i++;
	}
	return (0);
}
