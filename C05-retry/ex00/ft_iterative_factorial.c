int	ft_iterative_factorial(int nb)
{
	int x;

	x = nb;
	if (x < 0)
	{
		return (0);
	}
	if (x == 0)
		return (1);
	while (x > 1)
	{
		nb = nb * (x - 1);
		x--;
	}
	return (nb);
}
